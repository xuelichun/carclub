package com.zeepn.bean;

import org.apache.ibatis.type.Alias;
import org.springframework.stereotype.Component;
@Alias("PriView")
@Component
public class PriView {
	private int pri_pi;
	private int pri_vp1;
	private int pri_vp2;
	private int pri_cid;
	private String pri_cname;
	public PriView() {
	}
	public PriView(int pri_vp1, int pri_vp2, int pri_cid, String pri_cname) {
		this.pri_vp1 = pri_vp1;
		this.pri_vp2 = pri_vp2;
		this.pri_cid = pri_cid;
		this.pri_cname = pri_cname;
	}
	public PriView(int pri_pi, int pri_vp1, int pri_vp2, int pri_cid,
			String pri_cname) {
		this.pri_pi = pri_pi;
		this.pri_vp1 = pri_vp1;
		this.pri_vp2 = pri_vp2;
		this.pri_cid = pri_cid;
		this.pri_cname = pri_cname;
	}
	public int getPri_pi() {
		return pri_pi;
	}
	public void setPri_pi(int pri_pi) {
		this.pri_pi = pri_pi;
	}
	public int getPri_vp1() {
		return pri_vp1;
	}
	public void setPri_vp1(int pri_vp1) {
		this.pri_vp1 = pri_vp1;
	}
	public int getPri_vp2() {
		return pri_vp2;
	}
	public void setPri_vp2(int pri_vp2) {
		this.pri_vp2 = pri_vp2;
	}
	public int getPri_cid() {
		return pri_cid;
	}
	public void setPri_cid(int pri_cid) {
		this.pri_cid = pri_cid;
	}
	public String getPri_cname() {
		return pri_cname;
	}
	public void setPri_cname(String pri_cname) {
		this.pri_cname = pri_cname;
	}
	@Override
	public String toString() {
		return "PriView [pri_pi=" + pri_pi + ", pri_vp1=" + pri_vp1
				+ ", pri_vp2=" + pri_vp2 + ", pri_cid=" + pri_cid
				+ ", pri_cname=" + pri_cname + "]";
	}
}
