package com.zeepn.bean;
import org.apache.ibatis.type.Alias;
import org.springframework.stereotype.Component;
@Alias("TourSta")
@Component
public class TourSta {
	private int ts_id;
	private int dt_id;
	private int u_id;
	public TourSta() {
	}

	public TourSta(int ts_id, int dt_id, int u_id) {
		super();

		this.ts_id = ts_id;
		this.dt_id = dt_id;
		this.u_id = u_id;
	}

	public TourSta(int dt_id, int u_id) {

		this.dt_id = dt_id;
		this.u_id = u_id;
	}
	public int getTs_id() {
		return ts_id;
	}
	public void setTs_id(int ts_id) {
		this.ts_id = ts_id;
	}
	public int getDt_id() {
		return dt_id;
	}
	public void setDt_id(int dt_id) {
		this.dt_id = dt_id;
	}
	public int getU_id() {
		return u_id;
	}
	public void setU_id(int u_id) {
		this.u_id = u_id;
	}
	@Override
	public String toString() {
		return "TourSta [ts_id=" + ts_id + ", dt_id=" + dt_id + ", u_id="
				+ u_id + "]";
	}
	
}
