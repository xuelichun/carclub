package com.zeepn.bean;
import org.apache.ibatis.type.Alias;
import org.springframework.stereotype.Component;
@Component
@Alias("UserChacon")
public class UserChacon {
	private int uc_id;
	private	int ce_id;
	private int u_id;
	private int uc_value;
	public UserChacon() {
	}
	public UserChacon(int uc_id, int ce_id, int u_id, int uc_value) {
		super();
		this.uc_id = uc_id;
		this.ce_id = ce_id;
		this.u_id = u_id;
		this.uc_value = uc_value;
	}
	public UserChacon(int ce_id, int u_id, int uc_value) {
		super();
		this.ce_id = ce_id;
		this.u_id = u_id;
		this.uc_value = uc_value;
	}
	@Override
	public String toString() {
		return "UserChacon [uc_id=" + uc_id + ", ce_id=" + ce_id + ", u_id="
				+ u_id + ", uc_value=" + uc_value + "]";
	}
	public int getUc_id() {
		return uc_id;
	}
	public void setUc_id(int uc_id) {
		this.uc_id = uc_id;
	}
	public int getCe_id() {
		return ce_id;
	}
	public void setCe_id(int ce_id) {
		this.ce_id = ce_id;
	}
	public int getU_id() {
		return u_id;
	}
	public void setU_id(int u_id) {
		this.u_id = u_id;
	}
	public int getUc_value() {
		return uc_value;
	}
	public void setUc_value(int uc_value) {
		this.uc_value = uc_value;
	}
	
}
